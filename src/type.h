typedef unsigned char		u8;		// 符号なし８ビット整数
typedef unsigned short		u16;	// 符号なし１６ビット整数
typedef unsigned int		u32;	// 符号なし３２ビット整数
typedef unsigned long long	u64;	// 符号なし６４ビット整数
typedef signed char			s8;		// 符号あり８ビット整数
typedef signed short		s16;	// 符号あり１６ビット整数
typedef signed int			s32;	// 符号あり３２ビット整数
typedef signed long long	s64;	// 符号あり６４ビット整数
typedef float				f32;	// ３２ビット浮動小数
typedef double				f64;	// ６４ビット浮動小数

